import kotlinx.coroutines.delay
import kotlinx.coroutines.launch

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun AutoScrollingTextScreen() {
    val scrollState = rememberScrollState()
    val coroutineScope = rememberCoroutineScope()
    var isScrolling by remember { mutableStateOf(false) }

    Scaffold(
        topBar = {
            TopAppBar(title = { Text("Auto Scroll Text") })
        },
        floatingActionButton = {
            FloatingActionButton(
                onClick = {
                    // Toggle auto-scrolling state
                    isScrolling = !isScrolling
                    if (isScrolling) {
                        // Start auto-scroll
                        coroutineScope.launch {
                            while (isScrolling) {
                                val maxScroll = scrollState.maxValue
                                val currentScroll = scrollState.value

                                if (currentScroll < maxScroll) {
                                    scrollState.animateScrollTo(currentScroll + 50) // Scroll step
                                } else {
                                    isScrolling = false // Stop scrolling if at the bottom
                                }
                                delay(50L) // Adjust the delay for scrolling speed
                            }
                        }
                    }
                }
            ) {
                if (isScrolling) {
                    Text("Pause")
                } else {
                    Text("Play")
                }
            }
        }
    ) { paddingValues ->
        Column(
            modifier = Modifier
                .padding(paddingValues)
                .verticalScroll(scrollState)
                .fillMaxSize()
                .padding(16.dp)
        ) {
            // Long text content
            Text(
                text = generateLongText(),
                fontSize = 18.sp,
                textAlign = TextAlign.Justify
            )
        }
    }
}

